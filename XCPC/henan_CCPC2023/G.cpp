long long fastpow(long long num, long long b)
{
    long long ans = 1;
    while (b > 0)
    {
        if (ans > 1e18 / num)
            return -1;

        if (b % 2 == 1)
            ans = ans * num;
        b /= 2;
        if (b > 0 && num > 1e18 / num)
            return -1;
        num = num * num;
    }
    return ans;
}
